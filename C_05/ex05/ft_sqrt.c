int	ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb < 0)
		return (0);
	while (nb >= (i * i) && (i < 46341))
	{
		if (nb == (i * i))
			return (i);
		i ++;
	}
	return (0);
}
